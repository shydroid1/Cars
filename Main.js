let lexushover = document.getElementById("LexusLFA");
lexushover.addEventListener("mouseenter", function( event ) {
  event.target.style.filter = "grayscale(100%)"; 
  setTimeout(function(_r) {
    event.target.style.color = "white";
  }, 500);s
}, false);

